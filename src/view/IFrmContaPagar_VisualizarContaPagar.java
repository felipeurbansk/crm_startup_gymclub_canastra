    private void btnAgendarPagamento_VisualizarConta_RealizarPagamentoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgendarPagamento_VisualizarConta_RealizarPagamentoActionPerformed
        float saldoAtual = Float.valueOf(IFrmFinanceiro.spnCaixa_SaldoAtual.getValue().toString());
        float totalPagar = Float.valueOf(spnAgendarPagamento_VisualizarConta_Total.getValue().toString());

        if(totalPagar >  saldoAtual) {
            JOptionPane.showMessageDialog(this, "Saldo em caixa insulficiente para realizar pagamento.","ATENÇÃO",JOptionPane.INFORMATION_MESSAGE);
        }else{
            if(JOptionPane.showConfirmDialog(this, "Deseja confirmar o pagamento da conta "+conta.getFornecedor().getPessoa().getNome()+" ?","ATENÇÃO",JOptionPane.OK_CANCEL_OPTION) == JOptionPane.YES_OPTION){
              
                if(conta.atualizarSituacaoContaPagar(conta,2)){
                    conta.atualizaCaixa(conta.getContaPagarId(), FrmPrincipal.caixa.getCaixaId());
                    movimento = new Movimento(FrmPrincipal.caixa, tipoMovimento = new Tipomovimento().retornaTipoMovimento(1), null, conta.getTotal());
                    movimento.cadastraMovimento(movimento);
                    JOptionPane.showMessageDialog(this, "Pagamento realizado!","SUCESSO",JOptionPane.INFORMATION_MESSAGE);
                    dispose();
                }else{
                    JOptionPane.showMessageDialog(this, "Não foi possivel realizar o pagamento!!","ATENÇÃO",JOptionPane.ERROR_MESSAGE);
                }
            }else{
                JOptionPane.showMessageDialog(this, "Pagamento cancelado!!!");
            }
        }
        IFrmFinanceiro.atualizaAllTbl();
    }//GEN-LAST:event_btnAgendarPagamento_VisualizarConta_RealizarPagamentoActionPerformed

