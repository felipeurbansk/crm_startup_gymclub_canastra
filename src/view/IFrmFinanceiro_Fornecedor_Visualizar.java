    /*
        Instancia o formulario de Cadastro para ser realizado
        alterações no cadastro do fornecedor.
    */
    private void btnVisualizarFornecedor_AlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnVisualizarFornecedor_AlterarActionPerformed
        if(ifrmFornecedor_Cadastro == null){
            ifrmFornecedor_Cadastro = new IFrmFinanceiro_Fornecedor_Cadastro(f);
        }
        
        if(!ifrmFornecedor_Cadastro.isVisible()){
            FrmPrincipal.dskPrincipal.add(ifrmFornecedor_Cadastro);
            ifrmFornecedor_Cadastro.show();
        }
        
        try{
            ifrmFornecedor_Cadastro.setEnabled(true);
            ifrmFornecedor_Cadastro.toFront();
            dispose();
        }catch(Exception ex){
            ex.printStackTrace();
        }
    }//GEN-LAST:event_btnVisualizarFornecedor_AlterarActionPerformed

    private void btnVisualizarFornecedor_ExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnVisualizarFornecedor_ExcluirActionPerformed
        
        if(JOptionPane.showConfirmDialog(null, "Deseja realmente excluir o fornecedor\n"+f.getPessoa().getNome()+" ?") == JOptionPane.YES_OPTION){
        
            if(f.verificaExistenciaContaFornecedor(f.getFornecedorId())){
                JOptionPane.showMessageDialog(this, "Não é possivel apagar esse fornecedor pois existem contas pagas de serviços prestado por ele.");
            }else{
                p = f.getPessoa();
                f.excluirFornecedor(f);
                e.excluirEndereco(e);
                p.excluirPessoa(p);
                JOptionPane.showMessageDialog(this, "Fornecedor excluido com sucesso.");
                IFrmFinanceiro.atualizaTblFornecedor();
                dispose();
                IFrmFinanceiro.atualizaAllTbl();
            }
        }
    }//GEN-LAST:event_btnVisualizarFornecedor_ExcluirActionPerformed


