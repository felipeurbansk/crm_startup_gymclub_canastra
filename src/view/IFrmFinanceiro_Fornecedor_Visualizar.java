    /*
        Recebe o fornecedor localizado pelo seu id e insere
        os dados em seus respectivos campos
    */
    private void carregarFornecedor(){
        f = new Fornecedor();
        f = f.retornaFornecedor(this.fornecedorId);
        txtVisualizarFornecedor_RazaoSocial.setText(f.getRazao());
        txtVisualizarFornecedor_NomeFantasia.setText(f.getPessoa().getNome());
        frmtVisualizarFornecedor_Cnpj.setText(f.getCnpj());
        frmtVisualizarFornecedor_TelFixo.setText(f.getPessoa().getTelFixo());
        frmtVisualizarFornecedor_TelCel.setText(f.getPessoa().getTelCel());
        txtVisualizarFornecedor_Email.setText(f.getPessoa().getEmail());
        txtVisualizarFornecedor_Ramo.setText(f.getRamo());
        carregaEndereco();
    }
    /*
        Recebe o endereco localizado pelo pessoaId e insere
        os dados em seus respectivos campos
    */
    private void carregaEndereco(){
        e = new Endereco();
        e = e.retornaEndereco(f.getPessoa().getPessoaId());
        txtVisualizarFornecedor_Bairro.setText(e.getBairro());
        txtVisualizarFornecedor_Endereco.setText(e.getRua());
        txtVisualizarFornecedor_Bairro1.setText(String.valueOf(e.getNumero()));
        frmtVisualizarFornecedor_Cep.setText(e.getCep());
        txtVisualizarFornecedor_Estado.setText(e.getCidade().getEstado().getNome());
        txtVisualizarFornecedor_Cidade.setText(e.getCidade().getNome());
    }
    
    
