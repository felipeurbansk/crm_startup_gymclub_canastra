    private void btnSuporte_VisualizarChamado_VisualizarRespostaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuporte_VisualizarChamado_VisualizarRespostaActionPerformed
        int linha = tblSuporte_VisualizarChamado_Chamado.getSelectedRow();
        if(linha >= 0){
            id = Integer.valueOf(tblSuporte_VisualizarChamado_Chamado.getValueAt(linha, 0).toString());
            IFrmSuporte_VisualizarChamado_VisualizarResposta iFrmSuporte_VisualizarResposta = new IFrmSuporte_VisualizarChamado_VisualizarResposta(id);
            
            if(!iFrmSuporte_VisualizarResposta.isVisible()){
                FrmPrincipal.dskPrincipal.add(iFrmSuporte_VisualizarResposta);
                iFrmSuporte_VisualizarResposta.setLocation(
                   dskPrincipal.getWidth()/2 - iFrmSuporte_VisualizarResposta.getWidth()/2,
                   dskPrincipal.getHeight()/2 - iFrmSuporte_VisualizarResposta.getHeight()/2);
                iFrmSuporte_VisualizarResposta.show();
            }
            
            try{
                iFrmSuporte_VisualizarResposta.setSelected(true);
                iFrmSuporte_VisualizarResposta.toFront();
            }catch(Exception ex){
                ex.printStackTrace();
            }
        }else{
            JOptionPane.showMessageDialog(this, "Selecione uma resposta para vizualização", "ATENÇÃO", JOptionPane.ERROR_MESSAGE);
        }
        
    }//GEN-LAST:event_btnSuporte_VisualizarChamado_VisualizarRespostaActionPerformed

    private void btnSuporte_VisualizarChamado_ResponderActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuporte_VisualizarChamado_ResponderActionPerformed
        IFrmSuporte_VisualizarChamado_Responder iFrmSuporte_VisualizarChamado_Responder = new IFrmSuporte_VisualizarChamado_Responder(chamado.getChamadoId());
        
        if(!iFrmSuporte_VisualizarChamado_Responder.isVisible()){
            FrmPrincipal.dskPrincipal.add(iFrmSuporte_VisualizarChamado_Responder);
                iFrmSuporte_VisualizarChamado_Responder.setLocation(
                   dskPrincipal.getWidth()/2 - iFrmSuporte_VisualizarChamado_Responder.getWidth()/2,
                   dskPrincipal.getHeight()/2 - iFrmSuporte_VisualizarChamado_Responder.getHeight()/2);
                iFrmSuporte_VisualizarChamado_Responder.show();
        }
        
        try{
            iFrmSuporte_VisualizarChamado_Responder.setSelected(true);
            iFrmSuporte_VisualizarChamado_Responder.toFront();
        }catch(Exception ex){
            ex.printStackTrace();
        }
    }//GEN-LAST:event_btnSuporte_VisualizarChamado_ResponderActionPerformed

    private void btnSuporte_VisualizarChamado_FinalizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuporte_VisualizarChamado_FinalizarActionPerformed
        
        if(JOptionPane.showConfirmDialog(this, "Finalizar Chamado #"+chamado.getChamadoId()+" ?","ATENÇÃO",JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION){
            chamado.finalizaChamado(chamado);
            JOptionPane.showMessageDialog(this, "Chamado finalizado com sucesso!","SUCESSO",JOptionPane.INFORMATION_MESSAGE);
            dispose();
            IFrmSuporte.carregarTabelaChamados(null, -1);
        }else{
            JOptionPane.showMessageDialog(this, "Finalização de chamado cancelado!","ATENÇÃO",JOptionPane.INFORMATION_MESSAGE);
        }
        
    }//GEN-LAST:event_btnSuporte_VisualizarChamado_FinalizarActionPerformed


