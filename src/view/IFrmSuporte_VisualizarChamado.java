    public void populaCamposChamado(){
        txtSuporte_VisualizarChamado_Solicitante.setText("FALTA INTEGRAÇÃO");
        txtSuporte_VisualizarChamado_Responsavel.setText(chamado.getUsuario().getPessoa().getNome());
        txtSuporte_VisualizarChamado_Situacao.setText(chamado.getSituacao().getStatus());
        txtSuporte_VisualizarChamado_Assunto.setText(chamado.getAssunto());
        frmtSuporte_VisualizarChamado_DataCriacao.setText(dataExibicao.format(chamado.getDataCriacao()));
    }
    
    public static void popularTabelaChamado(){
        dtmChamado = (DefaultTableModel)tblSuporte_VisualizarChamado_Chamado.getModel();
        dtmChamado.setNumRows(0);
        i = resposta.retornaIteratorResposta(chamado.getChamadoId());
       
        Resposta r;
        while(i.hasNext()){
            r = (Resposta)i.next();
            dtmChamado.addRow(r.getRespostas());
        }
        
    }
    
    public IFrmSuporte_VisualizarChamado(int chamadoId) {
        initComponents();
        chamado = chamado.retornaChamado(chamadoId);
        populaCamposChamado();
        popularTabelaChamado();
        if(chamado.getSituacao().getSituacaoId() == 8){
            btnSuporte_VisualizarChamado_Finalizar.setVisible(false);
            btnSuporte_VisualizarChamado_Responder.setVisible(false);
            lblSuporte_VisualizarChamado_DataFinalizacao.setVisible(true);
            frmtSuporte_VisualizarChamado_DataFinalizacao.setVisible(true);
            frmtSuporte_VisualizarChamado_DataFinalizacao.setText(dataExibicao.format(chamado.getDataFim()));
        }else{
            lblSuporte_VisualizarChamado_DataFinalizacao.setVisible(false);
            frmtSuporte_VisualizarChamado_DataFinalizacao.setVisible(false);
        }
    }

