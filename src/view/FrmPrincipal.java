    private void btnFinanceiroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFinanceiroActionPerformed
        if(financeiro == null){
            financeiro = new IFrmFinanceiro(usuario);
        }
        if(!financeiro.isVisible()){
            dskPrincipal.add(financeiro);
            financeiro.setLocation(
                   dskPrincipal.getWidth()/2 - financeiro.getWidth()/2,
                   dskPrincipal.getHeight()/2 - financeiro.getHeight()/2);
            financeiro.show();
        }
        
        try{
            financeiro.toFront();
            financeiro.setSelected(true);
        }catch(Exception ex){
            ex.printStackTrace();
        }
    }//GEN-LAST:event_btnFinanceiroActionPerformed

    private void btnSairActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSairActionPerformed
        if(caixa != null){
            JOptionPane.showMessageDialog(this, "Impossivel finalizar o programa pois o caixa encontra-se aberto, favor fechar antes de finalizar a sessão.","ATENÇÃO",JOptionPane.ERROR_MESSAGE);
        }else{
            dispose();
            acesso.setDataFim(new Date(System.currentTimeMillis()));
            acesso.finalizaAcesso(acesso);
            DAOSistema.getSessionFactory().close();
        }
    }//GEN-LAST:event_btnSairActionPerformed

