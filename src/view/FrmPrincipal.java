    public FrmPrincipal(Usuario usuario) {
        
        if(usuario != null){
            initComponents();
            this.usuario = usuario;
            this.acesso = new Acesso(usuario, new Date(System.currentTimeMillis()));
            acesso.iniciaAcesso(acesso);
            lblPrincipal_Usuario.setText(this.usuario.getLogin());
            lblPrincipal_NomeUsuario.setText(this.usuario.getPessoa().getNome());
            lblPrincipal_InicioSessao.setText(formatoData.format(this.acesso.getDataInicio()));
            this.setDefaultCloseOperation(DO_NOTHING_ON_CLOSE);
        }else{
            JOptionPane.showMessageDialog(this, "Não é possivel acessar sistema sem estar logado. Favor, revisar esse procedimento","ATENÇÃO",JOptionPane.ERROR_MESSAGE);
            dispose();
        }
        atualizarInterface();
    }
    
    public static void atualizarInterface(){
        if(FrmPrincipal.caixa != null){
            lblPrincipal_InicioCaixa.setText(formatoData.format(FrmPrincipal.caixa.getDataInicio()));
        }else{
            lblPrincipal_InicioCaixa.setText("CAIXA FECHADO");
        }
    }
    
